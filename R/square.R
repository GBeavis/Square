#' @title Square
#'
#' @description Squares a number
#'
#' @param x a value to get squared
#'
#' @return The square of the input
#'
#' @examples square(2)
#'
#' @export square
square<-function(x){
  return(x^2)
}

#' @title Cube
#'
#' @description Cubes a number
#'
#' @param x a value to get cubed
#'
#' @return The cube of the input
#'
#' @examples cube(2)
#'
#' @export cube
cube<-function(x){
  return(x^3)
}

